          <?php
          $baseFolder = 'assets/img/galeri';
          $allFolders = array_filter(glob($baseFolder . '/*'), 'is_dir');

          // Urutan yang diinginkan
          $desiredOrder = ["Program Kerja Utama", "Program Kerja Tambahan", "Program Kerja Bantuan"];

          // Filter dan urutkan folder sesuai daftar di atas
          $folders = [];
          $allFiles = []; // Menampung semua file untuk kategori "All"

          foreach ($desiredOrder as $folderName) {
            $folderPath = $baseFolder . '/' . $folderName;
            if (in_array($folderPath, $allFolders)) {
              $folders[$folderName] = $folderPath;
              $files = glob($folderPath . '/*.{jpg,png,jpeg,gif,mp4}', GLOB_BRACE);

              foreach ($files as $file) {
                $allFiles[] = ['file' => $file, 'folder' => $folderName]; // Simpan file untuk filter "All"
              }
            }
          }

          // Acak urutan file untuk filter "All"
          shuffle($allFiles);
          ?>

          <!-- Filter Buttons -->
          <ul class="portfolio-filters isotope-filters" data-aos="fade-up" data-aos-delay="100">
            <li data-filter="*" class="filter-active">All</li>
            <?php foreach ($folders as $folderName => $folderPath):
              $filterClass = strtolower(str_replace(' ', '-', $folderName));
            ?>
              <li data-filter=".filter-<?php echo $filterClass; ?>"><?php echo $folderName; ?></li>
            <?php endforeach; ?>
          </ul>

          <div class="row gy-4 isotope-container" data-aos="fade-up" data-aos-delay="200">
            <!-- Tampilkan semua item dalam urutan acak untuk filter "All" -->
            <?php foreach ($allFiles as $fileInfo):
              $file = $fileInfo['file'];
              $folderName = $fileInfo['folder'];
              $filterClass = strtolower(str_replace(' ', '-', $folderName));
              $fileName = basename($file);
              $fileExt = pathinfo($file, PATHINFO_EXTENSION);
            ?>
              <div class="col-lg-4 col-md-6 portfolio-item isotope-item filter-<?php echo $filterClass; ?>">
                <?php if ($fileExt == 'mp4'): ?>
                  <video controls class="img-fluid">
                    <source src="<?php echo $file; ?>" type="video/mp4">
                    Your browser does not support the video tag.
                  </video>
                <?php else: ?>
                  <img src="<?php echo $file; ?>" class="img-fluid" alt="<?php echo $fileName; ?>">
                <?php endif; ?>

                <div class="portfolio-info">
                  <h4><?php echo $folderName; ?></h4>
                  <p><?php echo pathinfo($fileName, PATHINFO_FILENAME); ?></p>
                  <?php if ($fileExt == 'mp4'): ?>
                    <a href="<?php echo $file; ?>" title="<?php echo pathinfo($fileName, PATHINFO_FILENAME); ?>"
                      class="glightbox preview-link">
                      <i class="bi bi-play-circle"></i>
                    </a>
                  <?php else: ?>
                    <a href="<?php echo $file; ?>" title="<?php echo pathinfo($fileName, PATHINFO_FILENAME); ?>"
