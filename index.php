        <div class="row gy-4">
          <div class="row gy-4">
            <?php
            $folder = "assets/img/team/";
            $files = array_diff(scandir($folder), array('.', '..'));

            // Urutan jabatan sesuai prioritas
            $role_order = [
              "KETUA" => 1,
              "WAKIL KETUA" => 2,
              "SEKRETARIS I" => 3,
              "SEKRETARIS II" => 4,
              "BENDAHARA I" => 5,
              "BENDAHARA II" => 6,
              "ACARA" => 7,
              "HUMAS" => 8,
              "PDD" => 9,
              "LOGISTIK" => 10
            ];

            $team_members = [];

            foreach ($files as $file) {
              $info = pathinfo($file);
              if (isset($info['extension']) && in_array($info['extension'], ['jpg', 'png'])) {
                $nameAndRole = explode(" - ", $info['filename']);
                if (count($nameAndRole) == 2) {
                  $name = $nameAndRole[0];
                  $role = trim($nameAndRole[1]);
                } else {
                  $name = $info['filename'];
                  $role = "Anggota"; // Default role jika format tidak sesuai
                }

                // Simpan dalam array
                $team_members[] = [
                  'name' => $name,
                  'role' => $role,
                  'file' => $folder . $file,
                  'order' => $role_order[$role] ?? 99 // Jika jabatan tidak ada dalam daftar, diberi prioritas terakhir
                ];
              }
            }

            // Urutkan berdasarkan jabatan
            usort($team_members, function ($a, $b) {
              return $a['order'] - $b['order'];
            });

            // Tampilkan anggota yang sudah diurutkan
            $delay = 100;
            foreach ($team_members as $member) {
            ?>
              <div class="col-lg-3 col-md-6 d-flex align-items-stretch" data-aos="fade-up" data-aos-delay="<?= $delay ?>">
                <div class="team-member">
                  <div class="member-img">
                    <img src="<?= $member['file'] ?>" class="img-fluid" alt="<?= $member['name'] ?>">
                  </div>
                  <div class="member-info">
                    <h4><?= $member['name'] ?></h4>
                    <span><?= $member['role'] ?></span>
                  </div>
                </div>
              </div><!-- End Team Member -->
            <?php
              $delay += 100;
            }
            ?>
          </div>

